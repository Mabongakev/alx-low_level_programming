#include <stdio.h>
/**
 * add - a sum funct
 * @a: first element
 * @b: second element
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - a subtraction funct
 * @a: first element
 * @b: second element
 * Return: subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - a multiplication funct
 * @a: first element
 * @b: second element
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - a division funct
 * @a: first element
 * @b: second element
 * Return: division
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - a reminder funct
 * @a: first element
 * @b: second element
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
